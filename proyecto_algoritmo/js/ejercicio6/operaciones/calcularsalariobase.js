// modulo 1: Salario Base = valorHora × horasTrabajadas
export function calcularsalariobase(valorHora, horasTrabajadas) {
    // calcular el salario base del trabajador es por horas trabajabas y el valor por hora
    // retornamos el valor de hora y lo multiplicamos por las horas trabajadas para sacar el salario base del trabajador 
    return valorHora * horasTrabajadas;
}